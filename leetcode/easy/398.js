// https://leetcode.com/problems/find-the-difference/

/*
Given two strings s and t which consist of only lowercase letters.
String t is generated by random shuffling string s and 
then add one more letter at a random position.

Find the letter that was added in t.

Example:

Input:
s = "abcd"
t = "abcde"

Output:
e

Explanation:
'e' is the letter that was added.
*/

/**
 * @param {string} s
 * @param {string} t
 * @return {character}
 */
var findTheDifference = function(s, t) {
  var sChars = s.split('').sort()
  var tChars = t.split('').sort()
  var i;
  for (i = 0; i < sChars.length; i++) {
    if (tChars[i] !== sChars[i]) {
      return tChars[i]
    }
  }
  return tChars[i]
};

const str = "abcd"
const two = "abcde"

const str1 = "abcd"
const two1 = "eabcd"

const str2 = "abcd"
const two2 = "abecd"

const str3 = "abcd"
const two3 = "abced"

console.log(findTheDifference(str, two), 'e')
console.log(findTheDifference(str1, two1), 'e')
console.log(findTheDifference(str2, two2), 'e')
console.log(findTheDifference(str3, two3), 'e')
